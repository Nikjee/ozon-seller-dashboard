function t(key) {
  return messages[locale.value]?.[key] ?? key
}

watch(locale, (val) => {
  localStorage.setItem(LOCALE_KEY, val)
})

export function useI18n() {
  function toggle() {
    locale.value = locale.value === 'ru' ? 'en' : 'ru'
  }
  return { locale, t, toggle }
}
